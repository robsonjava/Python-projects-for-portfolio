# Igual a: a == b
# Diferente de: a != b
# Menor que: a < b
# Menor ou igual a: a <= b
# Maior que: a > b
# Maior ou igual a: a >= b

a = 200
b = 201

if b > a:
    print("Sim! B é maior que a.")
elif a == b:
    print("A é igual a B!")
elif a != b:
    print("A é diferente de B!")
else:
    print("Não! B não é maior que a.")
print("Fim do programa.")